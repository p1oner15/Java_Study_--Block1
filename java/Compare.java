public class Compare { // HOMEWORK
    public static void main(String[] args) {
        double d = 10;
        double b = 20;
        System.out.println(d + " > " + b + " = " + (d > b));
        System.out.println(d + " < " + b + " = " + (d < b));
        System.out.println(b + " >= " + d + " = " + (b >= d));
        System.out.println(b + " <= " + d + " = " + (b <= d));
        System.out.println(b + " == " + d + " = " + (b == d)); // Equality = Равенство
        System.out.println(b + " != " + d + " = " + (b != d));
    }
}
